public class MethodsTask {

    public static void evenOrOdd(int num) {

        if (num % 2 == 0) {
            System.out.println("This is an even number");
        } else {
            System.out.println("This is an odd number");
        }

    }


















    public static void main(String[] args) {

    evenOrOdd(2);


    }
}
